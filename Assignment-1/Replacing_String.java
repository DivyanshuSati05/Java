
public class Replacing_String {

	public static void main(String[] args) {
		String text = "I love Java Programming";
		
		String Replacechar = text.replace('a', '@');
		
		String Replacesubstring = text.replace("Java", "Python");
		
		System.out.println("Replace 'a' with '@': "+ Replacechar);
		System.out.println("Replace \"java\" with \"python\": "+ Replacesubstring);
		
		
		
		

	}

}
