
public class charat_Example {

	public static void main(String[] args) {
		String text  = "Java Programming";
		
		char ch1 = text.charAt(0);
		char ch2 = text.charAt(5);
		
		System.out.println("Character at index 0 = "+ ch1 );
		System.out.println("Character at index 5 = "+ ch2 );
		

	}

}
